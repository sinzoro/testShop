package ky_webProject;

public class ZB_Buy_Books {
	
	private String buy_id;
	private String book_name;
	private String book_productnum;
	private String book_order_state;
	private String book_delivery_state;
	private int book_cnt;
	
	public String getBuy_id() {
		return buy_id;
	}
	public void setBuy_id(String buy_id) {
		this.buy_id = buy_id;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_productnum() {
		return book_productnum;
	}
	public void setBook_productnum(String book_productnum) {
		this.book_productnum = book_productnum;
	}
	public String getBook_order_state() {
		return book_order_state;
	}
	public void setBook_order_state(String book_order_state) {
		this.book_order_state = book_order_state;
	}
	public String getBook_delivery_state() {
		return book_delivery_state;
	}
	public void setBook_delivery_state(String book_delivery_state) {
		this.book_delivery_state = book_delivery_state;
	}
	public int getBook_cnt() {
		return book_cnt;
	}
	public void setBook_cnt(int book_cnt) {
		this.book_cnt = book_cnt;
	}
	
}

